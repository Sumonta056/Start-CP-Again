void solve()
{
    ll n;
    cin >> n;
    vector<ll> arr(n);
    map<ll, ll> count;
    for (ll i = 0; i < n; i++)
    {
        cin >> arr[i];
        count[arr[i]]++;
    }

    for(auto it:count){
        cout << it.first << " : " << it.second << endl;
    }
}

