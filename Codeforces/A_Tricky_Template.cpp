const int N = 3e5, M = N;

void solve()
{
    int n;
    cin >> n;

    string a, b, c;
    cin >> a >> b >> c;

    bool possible = false;

    for (int i = 0; i < n; i++)
    {
        if (a[i] == c[i] || b[i] == c[i])
        {
            continue;
        }
        else
        {
            possible = true;
            break;
        }
    }

    if (possible)
        cout << "YES" << endl;
    else
        cout << "NO" << endl;
}

int main()
{
    ios_base::sync_with_stdio(0), cin.tie(0), cout.tie(0);
    srand(chrono::high_resolution_clock::now().time_since_epoch().count());

    int t = 1;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}
